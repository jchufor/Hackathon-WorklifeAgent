(function () {
  const detect = () => {
    const stored = localStorage.getItem("worklife.lang");
    if (stored === "ko" || stored === "en") return stored;
    const nav = (navigator.language || "en").toLowerCase();
    return nav.startsWith("ko") ? "ko" : "en";
  };
  window.WL_LANG = detect();
  window.WL_setLang = (lang) => {
    window.WL_LANG = lang;
    localStorage.setItem("worklife.lang", lang);
    document.documentElement.lang = lang;
    window.dispatchEvent(new CustomEvent("wl-lang-change", { detail: lang }));
  };
  window.t = (key) => {
    const dict = window.I18N[window.WL_LANG] || window.I18N.en;
    return dict[key] || window.I18N.en[key] || key;
  };
  document.documentElement.lang = window.WL_LANG;
})();
